#include "fdf.h"

int	ft_atoi_base(char *nb)
{
	int	res;
	int	j;
	int	i;

	res = 0;
	if (nb[0] != '0' || nb[1] != 'x')
		return (0);
	i = 2;
	while ((nb[i] >= '0' && nb[i] <= '9') || (nb[i] >= 'a' && nb[i] <= 'f')
			|| (nb[i] >= 'A' && nb[i] <= 'F'))
		i++;
	j = i - 3;
	i = 2;
	while ((nb[i] >= '0' && nb[i] <= '9') || (nb[i] >= 'a' && nb[i] <= 'f')
			|| (nb[i] >= 'A' && nb[i] <= 'F'))
	{
		if (nb[i] >= '0' && nb[i] <= '9')
			res += (nb[i] - 48) << j * 4;
		else if (nb[i] >= 'a' && nb[i] <= 'f')
			res += ((nb[i] - 'a') + 10) << j * 4;
		else if (nb[i] >= 'A' && nb[i] <= 'F')
			res += ((nb[i] - 'A') + 10) << j * 4;
		j--;
		i++;
	}
	return (res);
}
